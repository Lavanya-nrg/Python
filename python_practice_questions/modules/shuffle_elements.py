# Write a Python program to shuffle the elements of a given list. Use random.shuffle()
import random 

# Original list of numbers
nums = [1, 2, 3, 4, 5]
print("Original list:")
print(nums)

# Shuffle the list using random.shuffle()
random.shuffle(nums)
print("Shuffle list:")
print(nums)

# Original list of words
words = ['red', 'black', 'green', 'blue']
print("\nOriginal list:")
print(words)

# Shuffle the list of words using random.shuffle()
random.shuffle(words)
print("Shuffle list:")
print(words)
